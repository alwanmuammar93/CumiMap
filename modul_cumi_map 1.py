robust_scaler.fit(df_clean.loc[idx_train, FITUR])
df_robust = robust_scaler.transform(df_clean[FITUR])

scaler = MinMaxScaler()
scaler.fit(df_robust[df_clean.index.isin(idx_train)])
df_clean[FITUR] = scaler.transform(df_robust)

print("\n=== Fitur setelah preprocessing (Robust + MinMax) ===")
print(df_clean[FITUR].describe().round(3))
