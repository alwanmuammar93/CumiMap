from sklearn.cluster import KMeans
from sklearn.metrics import silhouette_score
from sklearn.preprocessing import RobustScaler
import matplotlib.pyplot as plt

# ── Ambil nilai asli sebelum scaling untuk clustering ────────
# Clustering butuh nilai interpretatif (kg, mg/m³, °C)
# bukan nilai yang sudah di-transformasi dua kali
FITUR_CLUSTER = [
    'hasil_tangkapan_kg',  # basis utama zona
    'klorofil_a',
    'kecepatan_arus',
    'suhu_permukaan',
    'tinggi_gelombang',
]

# Ambil dari df_clean sebelum kolom FITUR di-scale
# hasil_tangkapan_kg sudah di-clean (clamp fisik + IQR) di Step 2
# tidak perlu clip lagi — triple capping dihilangkan
df_cluster = df_clean[FITUR_CLUSTER].copy()

# ── RobustScaler khusus clustering ───────────────────────────
robust_cluster   = RobustScaler()
X_cluster_scaled = robust_cluster.fit_transform(df_cluster)

# ── Validasi K=3 dengan silhouette — bukan penentu K ─────────
# K=3 dipilih berdasarkan domain: Rendah / Sedang / Tinggi
# Silhouette hanya untuk konfirmasi bahwa K=3 masuk akal
K_FINAL = 3

inertia    = []
sil_scores = []
K_range    = range(2, 9)

for k in K_range:
    km     = KMeans(n_clusters=k, random_state=42, n_init=10)
    labels = km.fit_predict(X_cluster_scaled)
    inertia.append(km.inertia_)
    sil_scores.append(silhouette_score(X_cluster_scaled, labels))

# Plot Elbow & Silhouette
fig, axes = plt.subplots(1, 2, figsize=(12, 4))
axes[0].plot(K_range, inertia, 'bo-', linewidth=2)
axes[0].axvline(K_FINAL, color='red', linestyle='--', alpha=0.6, label=f'K={K_FINAL} (dipilih)')
axes[0].set_xlabel('Jumlah Cluster (K)')
axes[0].set_ylabel('Inertia')
axes[0].set_title('Elbow Method')
axes[0].legend()
axes[0].grid(True, alpha=0.3)

axes[1].plot(K_range, sil_scores, 'ro-', linewidth=2)
axes[1].axvline(K_FINAL, color='navy', linestyle='--', alpha=0.6, label=f'K={K_FINAL} (dipilih)')
axes[1].set_xlabel('Jumlah Cluster (K)')
axes[1].set_ylabel('Silhouette Score')
axes[1].set_title('Silhouette Score')
axes[1].legend()
axes[1].grid(True, alpha=0.3)

sil_k3 = sil_scores[K_FINAL - 2]  # index 0 = K=2
print(f"\nSilhouette score K=3 : {sil_k3:.4f}")
print(f"Silhouette score terbaik (K={K_range[np.argmax(sil_scores)]}): {max(sil_scores):.4f}")
if sil_k3 >= 0.35:
    print(f"  ✓ K=3 dapat diterima secara statistik")
else:
    print(f"  ⚠️  Silhouette K=3 di bawah 0.35 — cluster kurang terpisah")

plt.suptitle('CUMI-MAP | Penentuan Jumlah Zona (K=3 dipilih)', fontweight='bold')
plt.tight_layout()
plt.show()

