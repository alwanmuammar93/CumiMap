# ============================================================
# CUMI-MAP | Step 2: Preprocessing & Feature Engineering
# + Outlier Handling (Robust) — FIXED
#
# Perbaikan:
# 1. hasil_tangkapan_kg hanya di-clean, tidak di-scale (bukan fitur input)
# 2. IQR batas bawah di-floor ke 0 untuk fitur yang tidak mungkin negatif
# 3. Scaler di-fit HANYA pada train set → tidak ada data leakage
# ============================================================

import numpy as np
import pandas as pd
from sklearn.preprocessing import RobustScaler, MinMaxScaler
from sklearn.model_selection import train_test_split

df_clean = df.copy()

print("=== Missing Values ===")
print(df_clean.isnull().sum())

# ── LAYER 1: Filter nilai tidak mungkin secara sains ─────────
BATAS_FISIK = {
    'suhu_permukaan':     (25.0, 33.0),
    'salinitas':          (30.0, 37.0),
    'kecepatan_arus':     (0.0,  2.0),
    'klorofil_a':         (0.0,  5.0),
    'tinggi_gelombang':   (0.0,  4.0),
    'hasil_tangkapan_kg': (0.0,  20.0),  # di-clean tapi tidak di-scale
    'latitude':           (-5.5, -4.5),
    'longitude':          (118.5, 120.5),
}

print("\n=== LAYER 1: Filter Batas Fisik ===")
n_sebelum = len(df_clean)
for kolom, (batas_min, batas_max) in BATAS_FISIK.items():
    outlier  = ((df_clean[kolom] < batas_min) | (df_clean[kolom] > batas_max))
    n_outlier = outlier.sum()
    if n_outlier > 0:
        print(f"  {kolom:<25}: {n_outlier} nilai di luar [{batas_min}, {batas_max}]")
    df_clean[kolom] = df_clean[kolom].clip(batas_min, batas_max)
print(f"  → Semua nilai di-clamp ke batas fisik (tidak ada baris dihapus)")

# ── LAYER 2: IQR Capping ─────────────────────────────────────
# PERBAIKAN: Fitur non-negatif di-floor ke 0 agar batas bawah
# tidak turun ke negatif meskipun distribusinya right-skewed
FITUR_IQR_NON_NEGATIF = [
    'klorofil_a', 'tinggi_gelombang',
    'kecepatan_arus', 'hasil_tangkapan_kg'
]
FITUR_IQR_BEBAS = [
    'suhu_permukaan', 'salinitas'
]

print("\n=== LAYER 2: IQR Capping ===")
for kolom in FITUR_IQR_NON_NEGATIF + FITUR_IQR_BEBAS:
    Q1  = df_clean[kolom].quantile(0.25)
    Q3  = df_clean[kolom].quantile(0.75)
    IQR = Q3 - Q1
    batas_bawah = Q1 - 1.5 * IQR
    batas_atas  = Q3 + 1.5 * IQR

    # Floor batas bawah ke 0 untuk fitur non-negatif
    if kolom in FITUR_IQR_NON_NEGATIF:
        batas_bawah = max(batas_bawah, 0.0)

    n_outlier = ((df_clean[kolom] < batas_bawah) |
                 (df_clean[kolom] > batas_atas)).sum()
    df_clean[kolom] = df_clean[kolom].clip(batas_bawah, batas_atas)
    if n_outlier > 0:
        print(f"  {kolom:<25}: {n_outlier} outlier di-cap "
              f"[{batas_bawah:.2f}, {batas_atas:.2f}]")

# ── LAYER 3: Encode fitur siklikal ───────────────────────────
df_clean['fase_sin'] = np.sin(2 * np.pi * df_clean['fase_bulan'] / 4)
df_clean['fase_cos'] = np.cos(2 * np.pi * df_clean['fase_bulan'] / 4)
df_clean['jam_sin']  = np.sin(2 * np.pi * df_clean['jam_tangkap'] / 24)
df_clean['jam_cos']  = np.cos(2 * np.pi * df_clean['jam_tangkap'] / 24)
df_clean['arus_sin'] = np.sin(np.radians(df_clean['arah_arus']))
df_clean['arus_cos'] = np.cos(np.radians(df_clean['arah_arus']))

# ── LAYER 4: Split dulu, baru fit scaler ─────────────────────
# PERBAIKAN UTAMA: Scaler di-fit hanya pada train set
# hasil_tangkapan_kg tidak masuk FITUR — hanya basis label zona
